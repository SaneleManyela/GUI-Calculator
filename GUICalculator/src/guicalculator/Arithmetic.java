    private void btnEqualsActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEqualsActionPerformed
        try {
            lblCalculation.setText(lblCalculation.getText() + txtInput.getText());
        
            double x = Double.parseDouble(lblCalculation.getText().substring(0,
                            lblCalculation.getText().indexOf(" ")).trim());
        
            String[] str = lblCalculation.getText().split(" ");
        
            double y = Double.parseDouble(str[str.length - 1]);
        
            lblCalculation.setText(lblCalculation.getText() + " = ");
        
            switch(operator) {
                case '/':
                    if(y == 0) {
                        JOptionPane.showMessageDialog(this, "Cannot Divide By Zero", "ERROR", JOptionPane.ERROR_MESSAGE);
                    } else {
                        txtInput.setText(String.valueOf(roundOff(x / y)));
                    }
                    break;
                
                case '*':
                    txtInput.setText(String.valueOf(roundOff(x * y)));
                    break;
                
                case '-':
                    txtInput.setText(String.valueOf(roundOff(x - y)));
                    break;
            
                case '+':
                    txtInput.setText(String.valueOf(roundOff(x + y)));
                    break;
            
                case '%':
                    txtInput.setText(String.valueOf((int)(x % y)));
                    break;
            }
        } catch(HeadlessException | NumberFormatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnEqualsActionPerformed


