    private void btnNegateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNegateActionPerformed
        String strInput = txtInput.getText();
        
        if(strInput.contains(".")) {
            txtInput.setText(String.valueOf(Double.parseDouble(strInput) * -1));
        } else {
            try {
                txtInput.setText(String.valueOf(Integer.parseInt(strInput) * -1));
            } catch(NumberFormatException e) {
                JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_btnNegateActionPerformed

