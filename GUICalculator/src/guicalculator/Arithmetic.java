    private void btnReciprocalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnReciprocalActionPerformed
        try {
            if(!txtInput.getText().equals("0")) {
                lblCalculation.setText("1/(" + txtInput.getText() + ") = ");
                txtInput.setText(String.valueOf(roundOff(1/Double.parseDouble(
                    txtInput.getText()))));
            } else {
                txtInput.setText("Cannot divide by zero");
            }
        } catch(NumberFormatException e) {
            JOptionPane.showMessageDialog(this, e.getMessage(), "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnReciprocalActionPerformed

